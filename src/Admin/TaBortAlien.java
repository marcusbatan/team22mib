    private void btnTaBortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTaBortActionPerformed
        if(Validering.cbValueTrue(cmbAllaAliens)) {
        try {
            
            String alienNamn = idb.fetchSingle("SELECT NAMN FROM ALIEN WHERE ALIEN_ID = " + alienid);
            
            //idb.update("UPDATE ALIEN SET ANSVARIG_AGENT = NULL WHERE ANSVARIG_AGENT = " + alienid + ";");
            idb.delete("DELETE FROM BOGLODITE WHERE ALIEN_ID = " + alienid + ";");
            idb.delete("DELETE FROM WORM WHERE ALIEN_ID = " + alienid + ";");
            idb.delete("DELETE FROM SQUID WHERE ALIEN_ID = " + alienid + ";");
           
            idb.delete("DELETE FROM ALIEN WHERE ALIEN_ID = " + alienid);

            JOptionPane.showMessageDialog(null, "Alien:" + " " + alienNamn + " " + "har raderats från MIB systemet!");

        } 
        catch (InfException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        }
    }//GEN-LAST:event_btnTaBortActionPerformed

