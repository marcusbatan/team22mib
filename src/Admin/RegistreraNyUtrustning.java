    private void btnRegistreraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistreraActionPerformed
        if(Validering.textFaltHarVarde(txtbenamning)){
        int UtrID =0;
            try {
            UtrID = Integer.parseInt(idb.fetchSingle("SELECT MAX(UTRUSTNINGS_ID) FROM UTRUSTNING"));
            UtrID +=1;
            idb.insert("INSERT INTO UTRUSTNING "
            + "(UTRUSTNINGS_ID, BENAMNING) "
            + "VALUES (" + UtrID + ","
            + "'" + txtbenamning.getText() + "')");

            JOptionPane.showMessageDialog(null, "Utrustning har blivit registrerad");
        } 
        catch (InfException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        }
    }//GEN-LAST:event_btnRegistreraActionPerformed

